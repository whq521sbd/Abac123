package com.aotuo.vegetable.entity;

import java.util.List;

public class SortMessage {

	private String name;
	private String sortLetters;
	private String imageUrl;
	private int id;
	private int parent_id;
	private List<CitySubMessage> city;
	
	
	public List<CitySubMessage> getCity() {
		return city;
	}

	public void setCity(List<CitySubMessage> list) {
		this.city = list;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSortLetters() {
		return sortLetters;
	}

	public void setSortLetters(String sortLetters) {
		this.sortLetters = sortLetters;
	}

	public int getParent_id() {
		return parent_id;
	}

	public void setParent_id(int parent_id) {
		this.parent_id = parent_id;
	}

	@Override
	public String toString() {
		return "SortMessage [name=" + name + ", sortLetters=" + sortLetters
				+ ", imageUrl=" + imageUrl + ", id=" + id + "]";
	}
	
	
}
