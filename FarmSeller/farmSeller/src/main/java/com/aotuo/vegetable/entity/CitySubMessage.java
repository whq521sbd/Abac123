package com.aotuo.vegetable.entity;

public class CitySubMessage {
	private int id;
	private String name;

	public CitySubMessage(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
